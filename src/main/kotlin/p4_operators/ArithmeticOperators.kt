/*1- Aritmetiksel Operatörler - Arithmetic Operators

Matematiksel işlemleri yapmamızı sağlayan operatör tipimizdir.
+	Toplama (Addition)
-	Çıkarma (Subtraction)
*	Çarpma (Multiplication)
/	Bölme(Division)
%	Mod Alma (Modulus) */
fun main() {
    val num1 = 10
    val num2 = 4

    val result = num1 + num2
    val result2 = num1 - num2
    val result3 = num1 * num2
    val result4 = num1 / num2
    val result5 = num1 % num2

    println("Toplama:$result")
    println("Çıkarma:$result2")
    println("Çarpma:$result3")
    println("Bölme:$result4")
    println("ModAlma:$result5")
}